package it.almaviva.sbnweb.bean;

import java.util.List;

public class NoveCinquanta {
	List<String> esemplare;
	List<String> collocazione;
	List <String> inventario;
	
	public List<String> getEsemplare() {
		return esemplare;
	}
	public void setEsemplare(List<String> esemplare) {
		this.esemplare = esemplare;
	}
	public List<String> getCollocazione() {
		return collocazione;
	}
	public void setCollocazione(List<String> collocazione) {
		this.collocazione = collocazione;
	}
	public List<String> getInventario() {
		return inventario;
	}
	public void setInventario(List<String> inventario) {
		this.inventario = inventario;
	}
}
