echo "---------------------------------------------------------------------- "
echo "   Salvataggio base dati caricata... "
echo "---------------------------------------------------------------------- "

cd ${DIR}
tar cfvz ${COSA}_${ORARIO}.tar ${COSA}/*

echo "  " 
echo "---------------------------------------------------------------------- "
echo " Salvataggio completato " 
echo "---------------------------------------------------------------------- "




